#!/usr/bin/env bash
# =============================================================================
# operation/stop.sh  —  auth-server 중지
# -----------------------------------------------------------------------------
# 동작:
#   1) PID 파일이 없으면 이미 중지 상태 → 안전하게 통과(멱등)
#   2) PID 파일이 있고 프로세스가 살아있으면 종료
#   3) PID 파일이 있지만 프로세스가 없으면(비정상 종료 잔재) 파일만 정리
# =============================================================================
set -euo pipefail

RUN_DIR="$(dirname "$0")/.run"
PID_FILE="${RUN_DIR}/auth-server.pid"

# ── PID 파일 없음 → 이미 중지 ────────────────────────────────────────────────
if [ ! -f "$PID_FILE" ]; then
  echo "  ℹ PID 파일 없음 — 이미 중지 상태입니다."
  exit 0
fi

PID="$(cat "$PID_FILE")"

# ── 프로세스 종료 ─────────────────────────────────────────────────────────────
if kill -0 "$PID" 2>/dev/null; then
  kill "$PID"
  echo "  ✓ auth-server 종료 (PID ${PID})"
else
  echo "  ℹ PID ${PID} 프로세스가 이미 없습니다."
fi

rm -f "$PID_FILE"
echo "  · PID 파일 정리 완료"
